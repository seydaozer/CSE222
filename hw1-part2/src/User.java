public interface User {
	/**
	 * This method gets the name of the object.
	 * @return the name.
	 */
	String getName();
	
	/**
	 * This method sets the name of the object.
	 * @param name string of the name.
	 */
	void setName(String name);
	
	/**
	 * This method gets the surname of the object.
	 * @return the name.
	 */
	String getSurname();
	
	/**
	 * This method sets the surname of the object.
	 * @param surname string of the surname.
	 */
	void setSurname(String surname);
	
}
